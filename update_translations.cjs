languages.forEach(lang => {
  const filePath = path.join(i18nDir, lang + '.js');
  if (fs.existsSync(filePath)) {
    let content = fs.readFileSync(filePath, 'utf8');
    const newKeys = additions[lang];
    let toAppend = '';
    for (const [key, value] of Object.entries(newKeys)) {
      // replace existing if any
      const regex = new RegExp('^\\\\s*' + key + '\\\\s*:.*$', 'm');
      content = content.replace(regex, '');
      toAppend += '  ' + key + ': "' + value.replace(/"/g, '\\\\"') + '",\\n';
    }
    // inject before the last closing brace
    content = content.replace(/(};?\\s*)$/, ',\n' + toAppend + '$1');
    // clean up double commas or empty lines created
    content = content.replace(/,\\s*,/g, ',');
    fs.writeFileSync(filePath, content);
    console.log('Updated ' + lang);
  }
});
